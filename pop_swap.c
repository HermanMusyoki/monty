#include "monty.h"
/**
 * popFunc - a function that prints the top of the stack
 * @head: double pointer to the beginning of the stack
 * @counter: parameter for the line count
 * Return: nothing
*/
void popFunc(stack_t **head, unsigned int counter)
{
	stack_t *h;

	if (*head == NULL)
	{
		fprintf(stderr, "L%d: can't pop an empty stack\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	*head = h->next;
	free(h);
}

/**
 * swapFunc - a function that swaps the first two elements of the stack.
 * @head: double pointer to the beginning of the stack
 * @counter: parameter for the line count
 * Return: nothing
*/
void swapFunc(stack_t **head, unsigned int counter)
{
	stack_t *h;
	int len = 0, aux;

	h = *head;
	while (h)
	{
		h = h->next;
		len++;
	}
	if (len < 2)
	{
		fprintf(stderr, "L%d: can't swap, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	aux = h->n;
	h->n = h->next->n;
	h->next->n = aux;
}
